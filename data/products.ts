export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id)
}

export function getSets(): string[] {
  return Array.from(new Set(products.map(p => p.set)))
}

export function getRaretes(): string[] {
  return Array.from(new Set(products.map(p => p.rarity)))
}

export function getLangues(): string[] {
  return Array.from(new Set(products.map(p => p.language))).sort()
}
